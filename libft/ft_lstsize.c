#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		n;

	n = 0;
	if (lst)
	{
		n = 1;
		while (lst -> next)
		{
			lst = lst -> next;
			n += 1;
		}
	}
	return (n);
}
